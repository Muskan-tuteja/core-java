class Prog31{
public static void main(String [] args)
{
char ch = 'B';

if (ch >= 'A' && ch <= 'Z' &&
    !(ch == 'A' || ch == 'E' || ch == 'I' || ch == 'O' || ch == 'U')) {
    System.out.println("Character is an uppercase consonant");
} else {
    System.out.println("Character is not an uppercase consonant");
}










}
}
