class Prog30{
public static void main(String [] args)
{
char ch = 'b';

if ((ch >= 'A' && ch <= 'Z' || ch >= 'a' && ch <= 'z') &&
    !(ch == 'A' || ch == 'E' || ch == 'I' || ch == 'O' || ch == 'U' ||
      ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u')) {
    System.out.println("Character is a consonant");
} else {
    System.out.println("Character is not a consonant");
}









}
}
