class Program12 {

    public static void main(String[] args) {

        char ch = 'a';

        switch (ch) {

           case 'a': 
           case 'b': 
           case 'c': 
           case 'd': 
           case 'e':
           case 'f': 
           case 'g': 
           case 'h': 
          case 'i': 
          case 'j':
   
                System.out.println("lowercase character");
                break;

            default:
                System.out.println("Not an lowercase character");
        }
    }
}
